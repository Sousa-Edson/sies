    public void PreencheListaImagem() {

        MODELOIMG = new DefaultListModel();
        jList_Img.setModel(MODELOIMG);

        jC_Img_Caminho.removeAllItems();
        DaoImagem DImg = new DaoImagem();
        for (BeansImagem A : DImg.PreencheListaI(prod)) {
            MODELOIMG.addElement(A.getNome_imagem());
            jList_Img.setModel(MODELOIMG);

            String b = A.getCaminho_imagem();
            jC_Img_Caminho.addItem(String.valueOf(b));
            CaminhoImagem=b;
        }
    }


    public void chama(String Prod) {
        prod = Prod;
        DaoProduto daop = new DaoProduto();
        for (BeansProduto p : daop.Chama(prod)) {
            String st;
            int s = p.getProduto_categoria();
            if (s == 0) {

                st = "Uso interno";
            } else if (s == 1) {
                st = "Comun / Diversos";
            } else {
                st = "Kit / Agrupado";
            }
            id = p.getProduto_id();
            jT_categoria.setText(st);
            jT_produto.setText(p.getProduto_nome());
            jT_descricao.setText(p.getProduto_descricao());
            Double num4 = (p.getProduto_valor());
            BigDecimal df = new BigDecimal(num4);
            NumberFormat nf = NumberFormat.getInstance();
            nf.setMinimumFractionDigits(4);
            nf.setMaximumFractionDigits(4);
            String FormatoValorProd = nf.format(df);
            jF_valor.setText((FormatoValorProd));
            //jC_categoria.setSelectedIndex(p.getProduto_categoria());
            jF_ncm.setText(String.valueOf(p.getProduto_ncm()));

            Double num2 = (p.getProduto_min());
            BigDecimal df2 = new BigDecimal(num2);
            NumberFormat nf2 = NumberFormat.getInstance();
            nf2.setMinimumFractionDigits(3);
            nf2.setMaximumFractionDigits(3);
            String FormatoMin = nf2.format(df2);
            jF_min.setText(FormatoMin);

            String d = String.valueOf(p.getProduto_unidade());
            item = String.valueOf(p.getProduto_unidade());
            PreencheUnidadeB();
             CaminhoImagem ="";
            PreencheListaImagem();
//            CaminhoImagem = "src/Imagens/desert.jpg";
            imagem();
            PreencheTexto();
            PreencheListaB();
           // PreencheListaImagem();
            this.setTitle("  ID [" + id + "] - Produto - [" + p.getProduto_nome()+ " ]");
        }

    }


    public void Ajusta_Produto() {

        String s = jT_produto.getText();
        String textoLimitado = s.length() <= 90 ? s : s.substring(0, 100);
        if (textoLimitado.length() >= 99) {
            jLabelExibirInformacoes.setText("Campo com limite execido!! Max. " + textoLimitado.length());
            jT_produto.setText(textoLimitado);
        } else {
            jLabelExibirInformacoes.setText("Conta caracteres " + s.length() + " de 100");
        }
    }


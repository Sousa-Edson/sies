        BEANS.setProduto_categoria(jC_categoria.getSelectedIndex());
        BEANS.setProduto_nome(jT_produto.getText());
        BEANS.setProduto_unidade(id_unidade);
        String Valor = jF_valor.getText().replace(".", "").replace(",", ".");
        BEANS.setProduto_valor(Double.parseDouble(Valor));
        String Ncm = jF_ncm.getText().replace(".", "");

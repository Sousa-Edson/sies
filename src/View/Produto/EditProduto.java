    public void VerificarCamposVazios() {
        if (jF_valor.getText().trim().isEmpty()) {
            jF_valor.setText("0,000");
        }
        if (jF_min.getText().trim().isEmpty()) {
            jF_min.setText("1,000");
        }
        if (jT_produto.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Campo(s) vazio(s)");
            jT_produto.requestFocus();

        } else {
            jT_produto.setText(jT_produto.getText().toUpperCase());
            jT_descricao.setText(jT_descricao.getText().toUpperCase());

            Salvar();
        }
    }


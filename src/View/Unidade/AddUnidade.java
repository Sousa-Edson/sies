    public void ContaSigla() {
        String texto = jTextField1.getText();
        String textoLimitado = texto.length() <= 3 ? texto : texto.substring(0, 3);
        jTextField1.setText(textoLimitado);
        jTextField1.setText(jTextField1.getText().toUpperCase());
    }

    public void ContaDescricao() {
        String texto = jTextField2.getText();
        String textoLimitado = texto.length() <= 15 ? texto : texto.substring(0, 15);
        jTextField2.setText(textoLimitado);
        jTextField2.setText(jTextField2.getText().toUpperCase());
    }

    public void VerificarCamposVazios() {

        if (jTextField1.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Campo(s) vazio(s)");
            jTextField1.requestFocus();
        } else if (jTextField2.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Campo(s) vazio(s)");
            jTextField2.requestFocus();
        } else {
            Salvar();
        }
    }

    public void Salvar() {

        BEANS.setId_unidade(id_referencia);
        BEANS.setFrag_unidade((int) jComboBox1.getSelectedIndex());
        BEANS.setSigla_unidade(jTextField1.getText());
        BEANS.setDescricao_unidade(jTextField2.getText());
        BEANS.setId_usuario(Integer.parseInt(Menu.jLabelIdUsuario.getText()));
        BEANS.setStatus_unidade(1);
        DAO.Salvar(BEANS);
        LimpaCampos();
        ListaUnidade.jMenuItem4.doClick();
        JOptionPane.showMessageDialog(null, "Unidade cadastrado com sucesso. ");
        this.dispose();
    }

    public void LimpaCampos() {
        jTextField1.setText(null);
        jTextField2.setText(null);

        nome = "";
        login = "";
        senha = "";
        confirma = "";
        jComboBox1.requestFocus();
    }



    public void PreencheInformacao(String a, String b) {
        id = a;
        this.setTitle("  ID [" + a + "] ");
        if (b.equals("produto")) {
            DaoProduto di = new DaoProduto();
            String txt = di.RetornaInformacao(a);
            jEditorPane1.setText(txt);
            modo = 1;
        }
        if (b.equals("nota")) {
            DaoNota di = new DaoNota();
            String txt = di.RetornaInformacao(a);
            jEditorPane1.setText(txt);
            jB_Salvar.setVisible(true);
            jEditorPane1.setEditable(true);
            modo = 2;
        }
    }

    public void ManipulaTexto() {
        String s = jEditorPane1.getText();
        s = s.toUpperCase();
        txt = s.length() <= 400 ? s : s.substring(0, 400);
    }

    public void Salvar() {
        txt = jEditorPane1.getText().toUpperCase();
        if (modo == 1) {
            produto();
        } else if (modo == 2) {
            nota();
        } else {
            JOptionPane.showConfirmDialog(rootPane, "verifique");
        }
        this.dispose();
    }

    public void produto() {
        DaoProduto di = new DaoProduto();
        BeansProduto bi = new BeansProduto();
        bi.setProduto_id(Integer.parseInt(id));
        bi.setProduto_informacao(txt);
        di.UpdateInformacoes(bi);
    }

    public void nota() {
        DaoNota di = new DaoNota();
        BeansNota bi = new BeansNota();
        bi.setId_nota(Integer.parseInt(id));
        bi.setNota_infomacao(txt);
        di.UpdateInformacoes(bi);
    }


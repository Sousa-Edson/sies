    public void ChamaEmpresa() {
        conex.conexao();
        conex.executaSql2("SELECT  empresa_descricao ,empresa_cnpj FROM tbl_empresa where empresa_id is not null and empresa_id !=0 order by empresa_id asc ");
        try {
            conex.rs.last();
            MinhaEmpresa = conex.rs.getString("empresa_descricao");
            MeuCnpj = conex.rs.getString("empresa_cnpj");
            this.setTitle("SIES " + MinhaVersao + " -  " + MinhaEmpresa + " " + MeuCnpj);
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane, "NÃ£o foi encontrado uma empresa !");
        }
        conex.desconecta();

    }

    public void ValidaLogin() {

        int perm = Integer.parseInt(jLabelPermissao.getText());
        if (perm == 0) {
            jMenuItem2.setEnabled(true);
            jMenuItem4.setEnabled(true);
            jMenu6.setEnabled(true);
        } else if (perm == 1) {
            jMenuItem2.setEnabled(false);
            jMenuItem4.setEnabled(false);
            jMenu6.setEnabled(false);
        } else {
            jMenuItem2.setEnabled(false);
            jMenuItem4.setEnabled(false);
            jMenu6.setEnabled(false);
        }
    }

